pub trait Engine {
    fn process_a(
        &mut self, 
        _abuf: &mut msgf_afrm::AudioFrame) {}
    fn process_as(
        &mut self, 
        _in_abuf: [&mut msgf_afrm::AudioFrame;2]) {}
    fn process_c(
        &mut self,
        _cbuf: &mut msgf_cfrm::CtrlFrame) {}
    fn process_ac(
        &mut self,
        _abuf: &mut msgf_afrm::AudioFrame,
        _cbuf: &mut msgf_cfrm::CtrlFrame) {}
    fn process_as2(&mut self, 
        _in_abuf: [&mut msgf_afrm::AudioFrame;2], 
        _out_abuf: [&mut msgf_afrm::AudioFrame;2]) {}
}
